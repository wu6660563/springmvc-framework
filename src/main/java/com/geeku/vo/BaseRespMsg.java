package com.geeku.vo;


/**
 * 响应信息基础公共类
 * 
 * @author ji.jiang
 *
 */
@SuppressWarnings("serial")
public class BaseRespMsg extends BaseVo {
	
	public int id;//状态码
	
	public String msg;//提示消息
	
	public Object data;//响应数据

	public BaseRespMsg(int id, String msg, Object data){
		this.id = id;
		this.msg = msg;
		this.data = data;
	}

}
